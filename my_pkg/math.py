"""Math module with example functions. """

def add(num1, num2):
    """Add function."""
    return num1 + num2

def subtract(num1, num2):
    """Subtract function."""
    return num1 - num2
    
def multiply(num1, num2):
    """Multiply function."""
    return num1 * num2
    
def divide(num1, num2):
    """Divide function."""
    return num1 / num2
